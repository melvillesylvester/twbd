<h1>History</h1>
<p>Thy Will Be Done! Christian Ministries (hereinafter, "Thy Will Be Done!) was organized on April 6, 1930, on an Easter Sunday morning beginning with a sunrise service at 6 a.m.  People's Baptist Church, as it was formerly known, was located on East 98th Street and Third Avenue and formally incorporated in 1936.  Pursuing the love of God, Thy Will Be Done! set a standard of congregational involvement and the salvation of the lost.</p>
<p>Prompted by the City of New York in 1960, Thy Will Be Done! secured the present location of 165 East 103rd Street in East Harlem, where the first lot and building were purchased.  Through God's provision, the building next to the church was purchased, and the mortgage was extinguished.</p>
<p>To further the expansion of God's kingdom, Thy Will Be Done! purchased the two lots and building adjoining the sanctuary.  In 1985, the ministry was featured by the Christian Broadcasting Network on The 700 Club.  In 1991, Thy Will Be Done! became a non-denominational fellowship to more accurately reflect the message of Christ that salvation is in the name of Jesus Christ alone.  It also implemented its first Ministerial Training program and remains deeply committed to teaching God's Word to God's people and raising up future disciples that truly represent Christ.</p>
<p>Currently, Thy Will Be Done! is committed to building up the body of Christ through example, teaching and preaching the uncompromised Word of God.  The vision is simple but powerful: to see lives within and outside of Thy Will Be Done! Christian Ministries changed by the manifested power of God.</p>
