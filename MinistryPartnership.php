<h1>MINISTRY PARTNERSHIP</h1>
<p>Your interest in partnership is evidence of the fact that not only has this ministry affected your life, but you also have a desire to help someone else and believe this message can provide that help. Together, through partnership, you and I can reach more people than we could by laboring alone.</p>
<p>The Apostle Paul understood his role as the messenger, but he also understood that partnership was not only about what he received but what the partners received as well. In Philippians 4:15 he wrote, "No church communicated with me as concerning giving and receiving, but ye only."  This was written by Paul to his partners.  The Philippians were the only ones who supported him after leaving town.</p>
<p>Paul identified two laws of partnership.  First, it puts in operation the law of sowing and reaping.  As you give, God will guarantee that you shall receive, good measure, pressed down and shaken together."  Luke 6:38.  Second,  Paul was saying that there is a special provision for those who recognize the power in partnership.  He goes on to write, "My God shall supply all of your need according to his riches in glory by Christ Jesus."  Philippians 4:19.  The measure of your supply is linked to the divine supply that God has in heaven.</p>
<p>If you're a partner, then consider this our ministry, and together we will accomplish God's will. In fact, I and each minister, elder, deacon and member, are extensions of you and your obedience to God.  When we fulfill that call, the fruit will be evident to everyone.</p>
<p>We would consider it an honor to partner with you in this vision of taking the Gospel to the body of Christ in America and around the world.  Thank you for your support.<br/>
<br/>
Pastor Kevin</p>
