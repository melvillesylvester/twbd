<h1>Pastor's Pen</h1>
<p><img class="image_right image_b7" src="/images/Photo0357a.jpg" border="0">Dear Family Member:</p>
<p>I have always wondered about the idea of “God’s Will.”  Some Christians believe that everything happens within God’s will or is allowed by God to ensure that God’s will is done.  The first belief system holds that God causes each event in our lives–good or bad.  The other acknowledges that God does not cause everything to happen but may ALLOW other agents to create circumstances that will promote God’s will–even if these circumstances are tragic and against the revealed CHARACTER of God.
</p>
<p>Scripture reveals that God has a permissive will and an unalterable will for our lives and the destiny of mankind.  God often contends with Satan, our own disobedience or laziness and the curse of a fallen world which determine whether God’s perfect will is done in our lives.
</p>
<p>I ask you, can God’s will fail to be done?  Many believers would say “NO!”  However, scripture tells a different story.  <span style="font-weight: bold;">2 Peter 3:9</span> states, “The Lord is not slack concerning his promise, but is longsuffering to us, not willing that any should perish but that all come to repentance.”  Do all people repent?  If they did, hell would be empty.  Although God wills that all men be saved, God will not override our power of choice.
</p>
<p>In His model prayer, Jesus made the Father’s will our daily concern.  He taught us to pray, “Thy kingdom come, thy will be done.  On earth, as it is in heaven.”  Jesus knows that powerful natural and spiritual forces are arrayed against God and His will for our lives.  Satan, demons and believers themselves often work directly and indirectly to prevent God’s will from being accomplished.
</p>
<p>What does this all mean?  Everything that happens to us is not of God, is not ordained by God, is not authored or even approved by God.  Unrepentance, sickness, poverty and premature death are not of God.  Just as God is not willing that any should perish, He does not want any believer to live a defeated life.
</p>
<p>Your part is to pray and declare the promises of God concerning you and your family.  If you have not repented and changed your way of living, then repent and permanently change your lifestyle. Let Jesus become Lord of your life.  Find a promise that guarantees success and confess that WORD no matter what you see.  That’s doing your part!!!
</p>
<p>In Him,<br/><br/>
Pastor Kevin</p>
