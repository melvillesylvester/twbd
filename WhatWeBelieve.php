<h1>STATEMENT OF FAITH</h1>
<p><b>What We Believe!</b></p>
<ul class="decimal">
<li>The Bible is the inspired and only infallible Word of God.</li>
<li>There is one true God, eternally existent in three persons: God the Father, God the Son, God the Holy Spirit.</li>
<li>In the deity of our Lord Jesus Christ, in His virgin birth, in His sinless life, in His miracles, in His vicarious and atoning death, in His bodily resurrection, in His ascension to the right hand of the Father, in His personal future return to the earth in power and glory to rule for a thousand years.</li>
<li>In the blessed hope, the imminent return of Christ for overcoming believers.</li>
<li>In the fall and sinfulness of man and that the only means of being cleansed from sin is through repentance and faith in the redeeming blood of Christ.</li>
