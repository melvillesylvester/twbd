<li>Divine healing is available through the redemptive work of Christ on the cross.</li>
<li>The Baptism of the Holy Spirit, according to Acts 2:4, is given to believers who ask for it. The Holy Spirit in a person's life is evidenced by changes in his life, including a renewed love for God, and people, a commitment to the scriptures and holy living. The spirit filled believer has the capability of speaking with new tongues whether or not he or she chooses to exercise this gifting.</li>
<li>The Church of Jesus Christ is the universal, spiritual body of believers from every tribe, tongue, kindred and race of peoples, and is indwelt by God through the Holy Spirit and divinely empowered to fulfill ministry and her Great Commission on earth.</li>
<li>In the sanctifying power of the Holy Spirit by whose indwelling the Christian is enabled to live a holy life.</li>
<li>In the resurrection and final judgment of both the saved and the lost, the saved to everlasting life and the lost to everlasting damnation.</li>
<li>We practice two ordinances: (1) Water Baptism by immersion after repenting of one's sins and receiving the gift of salvation. And (2) Holy Communion (the Lord's Supper) as a symbolic remembrance of Christ's suffering and death for our salvation. </li>
